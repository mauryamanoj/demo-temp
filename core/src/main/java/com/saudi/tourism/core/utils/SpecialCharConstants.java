package com.saudi.tourism.core.utils;

/**
 * Special Character Constants class.
 * Dictionary for commonly used special character constants.
 */
public final class SpecialCharConstants {

  /**
   *  Opening bracket.
   **/
  public static final String OPEN_BRACKET = "(";

  /**
   *  closing bracket.
   **/
  public static final String CLOSE_BRACKET = ")";

  /**
   *  Forward slash.
   **/
  public static final String FORWARD_SLASH = "/";

  /**
   *  Colon symbol.
   **/
  public static final String COLON = ":";

  /**
   *  Double Forward slash.
   **/
  public static final String DOUBLE_FORWARD_SLASH = "//";

  /**
   *  Double Forward slash.
   **/
  public static final String DOT = ".";

  /**
   *  Pipe Separator.
   **/
  public static final String PIPE = "|";

  /**
   *  Double quotation escape symbol.
   **/
  public static final String DOUBLE_QUOTE = "\"";
  /**
   *  TWO.
   **/
  public static final int TWO = 2;
  /**
   *  THREE.
   **/
  public static final int THREE = 3;

  /**
   * private default constructor.
   */
  private SpecialCharConstants() {
    // do nothing constructor
  }
}
