package com.saudi.tourism.core.models.components.tripplan;

/**
 * Class - filter with getter/setter methods for fields 'locale' and 'path'.
 */
public interface PathLocaleFilter {

  /**
   * Getter for field 'locale'.
   * @return locale
   */
  String getLocale();

  /**
   * Setter method for field 'locale'.
   * @param locale locale
   */
  void setLocale(String locale);

  /**
   * Getter for field 'path'.
   * @return path
   */
  String getPath();

  /**
   * Setter method for field 'path'.
   * @param path locale
   */
  void setPath(String path);
}
